"""
Определения состояний конечного автомата (FSM) для управления пользовательскими сессиями.

Состояния описывают различные этапы взаимодействия пользователя с ботом:
- решение математических примеров
- ожидание ответов от пользователя
"""

from aiogram.fsm.state import State, StatesGroup


class UserStates(StatesGroup):
    """
    Состояния пользователя в процессе решения математического примера.

    Пользователь имеет три попытки на каждый пример:
    1. await_1_answer: Первая попытка (максимальный балл при успехе)
    2. await_2_answer: Вторая попытка (средний балл при успехе)
    3. await_3_answer: Третья и последняя попытка (минимальный балл или штраф)

    Attributes:
        solved: Состояние после успешного решения примера (используется для внутренних переходов)
        await_1_answer: Состояние ожидания первого ответа пользователя
        await_2_answer: Состояние ожидания второго ответа пользователя
        await_3_answer: Состояние ожидания третьего ответа пользователя
    """
    solved = State()
    await_1_answer = State()
    await_2_answer = State()
    await_3_answer = State()
